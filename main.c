void mix_columns(unsigned char* state) {
    unsigned char temp[16];

    // Perform mix colmuns using lookup tables
    for (int c = 0; c < 4; c++) {
        temp[c * 4 + 0] = gm_lookup_2[state[c * 4 + 0]] ^ gm_lookup_3[state[c * 4 + 1]] ^ state[c * 4 + 2] ^ state[c * 4 + 3];
        temp[c * 4 + 1] = state[c * 4 + 0] ^ gm_lookup_2[state[c * 4 + 1]] ^ gm_lookup_3[state[c * 4 + 2]] ^ state[c * 4 + 3];
        temp[c * 4 + 2] = state[c * 4 + 0] ^ state[c * 4 + 1] ^ gm_lookup_2[state[c * 4 + 2]] ^ gm_lookup_3[state[c * 4 + 3]];
        temp[c * 4 + 3] = gm_lookup_3[state[c * 4 + 0]] ^ state[c * 4 + 1] ^ state[c * 4 + 2] ^ gm_lookup_2[state[c * 4 + 3]];
    }

    for (int i = 0; i < 16; i++) {
        state[i] = temp[i];
    }
}

void inv_mix_columns(unsigned char* state) {
    unsigned char temp[16];

    // Perform reverse mix colmuns using lookup tables
    for (int c = 0; c < 4; c++) {
        temp[c * 4 + 0] = gm_lookup_14[state[c * 4 + 0]] ^ gm_lookup_11[state[c * 4 + 1]] ^ gm_lookup_13[state[c * 4 + 2]] ^ gm_lookup_9[state[c * 4 + 3]];
        temp[c * 4 + 1] = gm_lookup_9[state[c * 4 + 0]] ^ gm_lookup_14[state[c * 4 + 1]] ^ gm_lookup_11[state[c * 4 + 2]] ^ gm_lookup_13[state[c * 4 + 3]];
        temp[c * 4 + 2] = gm_lookup_13[state[c * 4 + 0]] ^ gm_lookup_9[state[c * 4 + 1]] ^ gm_lookup_14[state[c * 4 + 2]] ^ gm_lookup_11[state[c * 4 + 3]];
        temp[c * 4 + 3] = gm_lookup_11[state[c * 4 + 0]] ^ gm_lookup_13[state[c * 4 + 1]] ^ gm_lookup_9[state[c * 4 + 2]] ^ gm_lookup_14[state[c * 4 + 3]];
    }

    for (int i = 0; i < 16; i++) {
        state[i] = temp[i];
    }
}

void pad_block(unsigned char* block, unsigned int size) {
    block[size] = 0b10000000;
    for (unsigned int i = size + 1; i < 16; i++) {
        block[i] = 0;
    }
}

// Check if block contains pading signature and return unpaded block
unsigned int unpad_block(unsigned char* block) {
    unsigned int i = 15;
    while (i > 0) {
        if (block[i] == 0x80)
            return i;
        i--;
    }
    return 15;
}

unsigned int alloc_blocks(const char* msg, const unsigned int size) {
    unsigned int   block_count = size / 16 + (size % 16 != 0 ? 1 : 0);
    unsigned char* blocks = (unsigned char*)global_state->encryption_state;
    for (unsigned int i = 0; i < block_count; i++) {
        unsigned int copy_size = (i == block_count - 1 && size % 16 != 0) ? size % 16 : 16;
        for (unsigned int j = 0; j < copy_size; j++) {
            blocks[i * 16 + j] = (unsigned char)msg[i * 16 + j];
        }
        if (copy_size < 16) {
            pad_block(&blocks[i * 16], copy_size);
        }
    }
    return block_count;
}

void rounds(unsigned char* key, const unsigned char* last_state, unsigned char* state) {
    if (last_state != NULL) {
        for (int i = 0; i < 16; i++) {
            state[i] ^= last_state[i];
        }
    }
    // initial round key addition
    add_round_key(key, state);

    // 9 rounds
    for (int round = 1; round <= 9; round++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);

        const unsigned char* round_key = key + round * 16;
        add_round_key(round_key, state);
    }

    // final round, i.e; 10 rounds
    sub_bytes(state);
    shift_rows(state);
    const unsigned char* round_key = key + 10 * 16;
    add_round_key(round_key, state);
}

void inv_rounds(unsigned char* key, unsigned char* last_state, unsigned char* state) {
    // initial round key addition
    const unsigned char* round_key = key + 10 * 16;
    add_round_key(round_key, state);
    inv_shift_rows(state);
    inv_sub_bytes(state);

    // 9 rounds
    for (int round = 9; round >= 1; round--) {
        round_key = key + round * 16;
        add_round_key(round_key, state);
        inv_mix_columns(state);
        inv_shift_rows(state);
        inv_sub_bytes(state);
    }

    // final round, i.e; 10 rounds
    round_key = key;
    add_round_key(round_key, state);

    if (last_state != NULL) {
        for (int i = 0; i < 16; i++) {
            state[i] ^= last_state[i];
        }
    }
}

void encrypt(unsigned char* key, void* data, unsigned int size, unsigned int* out_block_count) {
    unsigned int block_count = alloc_blocks(data, size);
    *out_block_count = block_count;
    unsigned char* last_state = NULL;
    unsigned char* state = (unsigned char*)global_state->encryption_state;

    rounds(key, last_state, state);
    if (is_cbc)
        last_state = state;

    for (int i = 1; i < block_count; i++) {
        rounds(key, last_state, &state[i * 16]);
        if (is_cbc)
            last_state = &state[(i - 1) * 16];
    }
}

unsigned char* decrypt(unsigned char* key, unsigned int block_count) {
    unsigned char* decrypt_state = (unsigned char*)global_state->decryption_state;
    unsigned char* state = (unsigned char*)global_state->encryption_state;
    for (int j = 0; j < 16 * block_count; j++) {
        decrypt_state[j] = state[j];
    }

    unsigned char* last_state = NULL;

    inv_rounds(key, last_state, &decrypt_state[0]);
    if (is_cbc)
        last_state = state;
    for (int i = 1; i < block_count; i++) {
        inv_rounds(key, last_state, &decrypt_state[i * 16]);
        if (is_cbc)
            last_state = &state[(i - 1) * 16];
    }

    return decrypt_state;
}

