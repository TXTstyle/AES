void key_expansion(const unsigned char* key, unsigned char* expanded_keys) {
    // copy over the original key
    for (int i = 0; i < 16; i++) {
        expanded_keys[i] = key[i];
    }

    for (unsigned int i = 4; i < 44; i++) {
        char temp[4];
        // copy previous word
        for (int j = 0; j < 4; j++) {
            temp[j] = expanded_keys[(i - 1) * 4 + j];
        }

        // Apply core transformations
        if (i % 4 == 0) {
            // rotate
            char t = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = t;

            // substitute bytes
            for (int j = 0; j < 4; j++) {
                temp[j] = sbox[(unsigned char)temp[j]];
            }

            // XOR with round constant
            temp[0] ^= rcon[i / 4];
        }

        // XOR with word 4 positions earlier
        for (int j = 0; j < 4; j++) {
            expanded_keys[i * 4 + j] = expanded_keys[(i - 4) * 4 + j] ^ temp[j];
        }
    }
}

void add_round_key(const unsigned char* key, unsigned char* state) {
    for (int i = 0; i < 16; i++) {
        state[i] ^= key[i];
    }
}

void sub_bytes(unsigned char* state) {
    for (int i = 0; i < 16; i++) {
        state[i] = sbox[state[i]];
    }
}

void inv_sub_bytes(unsigned char* state) {
    for (int i = 0; i < 16; i++) {
        state[i] = rsbox[state[i]];
    }
}

